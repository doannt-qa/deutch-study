def fill_file(path: Path) -> int:
    text = path.read_text(encoding='utf-8')
    count = 0

    def replacer(m: re.Match) -> str:
        nonlocal count
        full = m.group(0)
        entry_id = m.group(1)
        trans = TRANSLATIONS.get(entry_id, '')
        if not trans:
            return full
        escaped = trans.replace("'", "\\'")
        count += 1
        return full.replace("english: ''", f"english: '{escaped}'")

    new_text = re.sub(
        r"\{ id: '([^']+)',\s*german: '[^']*'.*?english: ''",
        replacer,
        text,
        flags=re.DOTALL,
    )

    path.write_text(new_text, encoding='utf-8')
    print(f'{path.name}: filled {count} entries.')
    return count


def main() -> None:
    if len(sys.argv) < 2:
        print(__doc__)
        sys.exit(1)
    for arg in sys.argv[1:]:
        p = Path(arg)
        if not p.exists():
            print(f'File not found: {p}', file=sys.stderr)
            continue
        fill_file(p)


if __name__ == '__main__':
    main()
